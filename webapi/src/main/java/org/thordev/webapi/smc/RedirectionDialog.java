    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        if (url != null)
			textUrl.setText(url);
		if (info == null)
			info = new RedirectionInfo();
		textRole.setText(StringUtil.join(info.roles));
		textUser.setText(StringUtil.join(info.users));
    }//GEN-LAST:event_formWindowOpened

    private void buttonOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonOkActionPerformed
        if (textUrl.getText().trim().isEmpty()) {
			JOptionPane.showMessageDialog(null, 
					"Must specify redirection URL!", 
					"Error", JOptionPane.WARNING_MESSAGE);
			textUrl.requestFocus(true);
			return;
		}
		if (!textUrl.getText().trim().equals(url) && 
				keySet.contains(textUrl.getText().trim())) {
			JOptionPane.showMessageDialog(null, 
					"URL already exist!", 
					"Error", JOptionPane.WARNING_MESSAGE);
			textUrl.requestFocus(true);
			return;
		}
		if (textRole.getText().trim().isEmpty() && textUser.getText().trim().isEmpty()) {
			JOptionPane.showMessageDialog(null, 
					"Must specify role or user!", 
					"Error", JOptionPane.WARNING_MESSAGE);
			textRole.requestFocus(true);
			return;
		}
		url = textUrl.getText().trim();
		info.roles = StringUtil.toStringSet(textRole.getText().trim());
		info.users = StringUtil.toStringSet(textUser.getText().trim());
		isOK = true;
		this.dispose();
    }//GEN-LAST:event_buttonOkActionPerformed

