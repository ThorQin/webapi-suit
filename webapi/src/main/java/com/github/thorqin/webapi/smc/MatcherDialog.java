	class SessionVariableModel extends MapTableModelBase<Set<String>> {
		private final Map<String, Set<String>> items;
		public SessionVariableModel(Map<String, Set<String>> items) {
			super(items, new String[]{"Session Variable", "Value Should Contained"});
			this.items = items;
		}
		public Map<String, Set<String>> getItems() {
			return this.items;
		}
		@Override
		protected String getColValue(int columnIndex, Map.Entry<String, Set<String>> entry) {
			switch (columnIndex) {
				case 0:
					return entry.getKey();
				case 1:
					return StringUtil.join(entry.getValue());
				default:
					return null;
			}
		}

	}
	class RedirectionModel extends MapTableModelBase<RedirectionInfo> {
		private final Map<String, RedirectionInfo> items;
		public RedirectionModel(Map<String, RedirectionInfo> items) {
			super(items, new String[]{"Redirection URL", "Roles", "Users"});
			this.items = items;
		}
		public Map<String, RedirectionInfo> getItems() {
			return this.items;
		}
		@Override
		protected Object getColValue(int columnIndex, Map.Entry<String, RedirectionInfo> entry) {
			switch (columnIndex) {
				case 0:
					return entry.getKey();
				case 1:
					return entry.getValue().roles;
				case 2:
					return entry.getValue().users;
				default:
					return null;
			}
		}

	}
	
