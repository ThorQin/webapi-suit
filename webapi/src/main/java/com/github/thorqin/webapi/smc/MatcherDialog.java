    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
		ok = false;
		
		if (matcher == null)
		   matcher = new URLMatcher();
		textMatcherName.setText(matcher.name);
		textDescription.setText(matcher.description);
		textURL.setText(StringUtil.join(matcher.url));
		textProtocol.setText(StringUtil.join(matcher.scheme));
		textDomain.setText(StringUtil.join(matcher.domain));
		textMethod.setText(StringUtil.join(matcher.method));
		textPort.setText(StringUtil.join(matcher.port));
		
		textResourceType.setText(matcher.resType);
		textResourceID.setText(matcher.resId);
		textOperation.setText(matcher.operation);
		textScenario.setText(matcher.scenario);
		sessionModel = new SessionVariableModel(Serializer.copy(matcher.sessionVariables));
		redirectionModel = new RedirectionModel(Serializer.copy(matcher.redirection));
		tableSessionVariable.setModel(sessionModel);
		tableRedirections.setModel(redirectionModel);
		textMatcherName.requestFocus();
    }//GEN-LAST:event_formWindowOpened

    private void buttonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonOKActionPerformed
        if (textMatcherName.getText().trim().isEmpty()) {
			JOptionPane.showMessageDialog(null, 
					"Must specify name!", 
					"Error", JOptionPane.WARNING_MESSAGE);
			panelSwitch.setSelectedIndex(0);
			textMatcherName.requestFocus(true);
			return;
		}
		if (textDescription.getText().trim().isEmpty()) {
			JOptionPane.showMessageDialog(null, 
					"Must specify description!", 
					"Error", JOptionPane.WARNING_MESSAGE);
			panelSwitch.setSelectedIndex(0);
			textDescription.requestFocus(true);
			return;
		}
		if (textURL.getText().trim().isEmpty()) {
			JOptionPane.showMessageDialog(null, 
					"Must provide URL rule!", 
					"Error", JOptionPane.WARNING_MESSAGE);
			panelSwitch.setSelectedIndex(0);
			textURL.requestFocus(true);
			return;
		}

		String name = textMatcherName.getText().trim();
		for (URLMatcher m : matcherList) {
			if (!m.equals(matcher) && m.name.equals(name)) {
				JOptionPane.showMessageDialog(null, 
					"Must specify unique matcher name!", 
					"Error", JOptionPane.WARNING_MESSAGE);
				panelSwitch.setSelectedIndex(0);
				textMatcherName.requestFocus(true);
				return;
			}
		}
		matcher.name = name;
		matcher.description = textDescription.getText();
		matcher.url = StringUtil.toStringSet(textURL.getText());
		matcher.scheme = StringUtil.toStringSet(textProtocol.getText().toLowerCase());
		matcher.domain = StringUtil.toStringSet(textDomain.getText().toLowerCase());
		matcher.method = StringUtil.toStringSet(textMethod.getText().toLowerCase());
		matcher.port = StringUtil.toStringSet(textPort.getText());
		
		matcher.resType = textResourceType.getText().trim();
		matcher.resId = textResourceID.getText();
		matcher.operation = textOperation.getText();
		matcher.scenario = textScenario.getText();
		matcher.sessionVariables = sessionModel.getItems();
		matcher.redirection = redirectionModel.getItems();
		ok = true;
		this.dispose();
    }//GEN-LAST:event_buttonOKActionPerformed

