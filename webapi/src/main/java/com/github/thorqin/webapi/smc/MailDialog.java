	private String generateKey() {
		String name = "default";
		String newKey = name;
		int i = 1;
		while (keys.contains(newKey)) {
			newKey = name + i;
			i++;
		}
		return newKey;
	}
	
    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        if (mailItem == null) {
			mailItem = new MailSettingItem();
		}
		if (key == null) {
			textConfigName.setText(generateKey());
		} else {
			textConfigName.setText(key);
			textConfigName.setEnabled(false);
		}
		textHost.setText(mailItem.host);
		textPort.setText(String.valueOf(mailItem.port));
		textUser.setText(mailItem.user);
		textPassword.setText(mailItem.password);
		textFrom.setText(mailItem.from);
		comboSecurity.setSelectedItem(mailItem.secure);
    }//GEN-LAST:event_formWindowOpened


