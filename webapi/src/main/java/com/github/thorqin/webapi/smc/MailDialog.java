    private void buttonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonOKActionPerformed
        if (textConfigName.getText().trim().isEmpty()) {
			JOptionPane.showMessageDialog(null,
                "Must specify config name!",
                "Error", JOptionPane.WARNING_MESSAGE);
            textConfigName.requestFocus(true);
            return;
		}
		if (keys != null && keys.contains(textConfigName.getText().trim())) {
			JOptionPane.showMessageDialog(null,
                "Config name already exists!",
                "Error", JOptionPane.WARNING_MESSAGE);
            textConfigName.requestFocus(true);
            return;
		}
		if (textHost.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                "Must specify server host!",
                "Error", JOptionPane.WARNING_MESSAGE);
            textHost.requestFocus(true);
            return;
        }
		if (!StringUtil.isInteger(textPort.getText().trim())) {
            JOptionPane.showMessageDialog(null,
                "Must specify server port(an integer value)!",
                "Error", JOptionPane.WARNING_MESSAGE);
            textPort.requestFocus(true);
            return;
        }
		if (textUser.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                "Must specify login user!",
                "Error", JOptionPane.WARNING_MESSAGE);
            textUser.requestFocus(true);
            return;
        }
		if (textPassword.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                "Must specify login password!",
                "Error", JOptionPane.WARNING_MESSAGE);
            textPassword.requestFocus(true);
            return;
        }
		
		key = textConfigName.getText();
		mailItem.host = textHost.getText().trim();
		mailItem.port = Integer.parseInt(textPort.getText());
		mailItem.user = textUser.getText().trim();
		mailItem.password = textPassword.getText().trim();
		mailItem.from = textFrom.getText().trim();
        mailItem.secure = (String)comboSecurity.getSelectedItem();
        ok = true;
        this.dispose();
    }//GEN-LAST:event_buttonOKActionPerformed

